CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_listarProductosCompradosPorUsuario`(
    IN p_idUsuario INT
)
BEGIN
    SELECT 
        p.nombreProducto AS nombre,
        p.descripcion,
        c.categoria,
        m.marca,
        p.valoracion AS valoracion_producto,
        p.precio,
        p.imagen
    FROM (
        SELECT 
            up.idProducto,
            MAX(comp.fechaCompra) AS ultima_fecha_compra
        FROM UsuarioProducto up
        INNER JOIN Detalle d ON up.idUsuarioProducto = d.idUsuarioProducto
        INNER JOIN Compra comp ON d.idCompra = comp.idCompra
        INNER JOIN UsuarioEstablecimiento ue ON comp.idUsuarioEstablecimiento = ue.idUsuarioEstablecimiento
        WHERE ue.idUsuario = p_idUsuario
        GROUP BY up.idProducto
    ) ultimos_productos
    INNER JOIN UsuarioProducto up ON ultimos_productos.idProducto = up.idProducto
    INNER JOIN Detalle d ON up.idUsuarioProducto = d.idUsuarioProducto
    INNER JOIN Compra comp ON d.idCompra = comp.idCompra
    INNER JOIN UsuarioEstablecimiento ue ON comp.idUsuarioEstablecimiento = ue.idUsuarioEstablecimiento
    INNER JOIN Producto p ON up.idProducto = p.idProducto
    INNER JOIN Categoria c ON p.idCategoria = c.idCategoria
    INNER JOIN Marca m ON p.idMarca = m.idMarca
    WHERE ue.idUsuario = p_idUsuario
    AND comp.fechaCompra = ultimos_productos.ultima_fecha_compra
    ORDER BY p.nombreProducto;
END ;;
