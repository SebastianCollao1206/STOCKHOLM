CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_estadisticaComprasMensualesPorAñoDeUsuario`(
    IN p_idUsuario INT,
    IN p_año INT
)
BEGIN
    SELECT 
        mes.numero_mes,
        mes.nombre_mes,
        COALESCE(compras.total_compras, 0) as total_compras,
        COALESCE(compras.monto_total, 0.00) as monto_total,
        COALESCE(compras.cantidad_productos, 0) as cantidad_productos
    FROM (
        SELECT 1 as numero_mes, 'Enero' as nombre_mes UNION ALL
        SELECT 2, 'Febrero' UNION ALL
        SELECT 3, 'Marzo' UNION ALL
        SELECT 4, 'Abril' UNION ALL
        SELECT 5, 'Mayo' UNION ALL
        SELECT 6, 'Junio' UNION ALL
        SELECT 7, 'Julio' UNION ALL
        SELECT 8, 'Agosto' UNION ALL
        SELECT 9, 'Septiembre' UNION ALL
        SELECT 10, 'Octubre' UNION ALL
        SELECT 11, 'Noviembre' UNION ALL
        SELECT 12, 'Diciembre'
    ) mes
    LEFT JOIN (
        SELECT 
            MONTH(co.fechaCompra) as mes,
            COUNT(co.idCompra) as total_compras,
            SUM(co.total) as monto_total,
            SUM(d.cantidad) as cantidad_productos
        FROM Compra co
        INNER JOIN UsuarioEstablecimiento ue ON co.idUsuarioEstablecimiento = ue.idUsuarioEstablecimiento
        INNER JOIN Detalle d ON co.idCompra = d.idCompra
        WHERE ue.idUsuario = p_idUsuario 
        AND YEAR(co.fechaCompra) = p_año
        GROUP BY MONTH(co.fechaCompra)
    ) compras ON mes.numero_mes = compras.mes
    ORDER BY mes.numero_mes;
END ;;
