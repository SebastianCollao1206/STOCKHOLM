CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_relacionarUsuarioACategoriaPorId`(in p_idCategoria int, in p_idUsuario int)
begin
	declare v_idProducto int;
	-- Buscar producto vacío del usuario con esa categoría
	select p.idProducto into v_idProducto
	from Producto p
	join UsuarioProducto up on up.idProducto = p.idProducto
	where up.idUsuario = p_idUsuario and p.idCategoria = p_idCategoria
	and p.nombreProducto = '' and p.descripcion = '' and p.idMarca is null
	and p.precio = 0.0 and p.valoracion = 0.0 and p.imagen = ''
	limit 1;
	-- Si no existe, crear uno nuevo
	if v_idProducto is null then
		insert into Producto (idCategoria, nombreProducto, descripcion, idMarca, precio, valoracion, imagen)
		values (p_idCategoria, '', '', null, 0.0, 0.0, '');
		set v_idProducto = last_insert_id();
		insert into UsuarioProducto (idUsuario, idProducto, stock)
		values (p_idUsuario, v_idProducto, 0);
	end if;
end ;;
