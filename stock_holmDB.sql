CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_listarInventarioDeUsuario`(
    IN p_idUsuario INT
)
BEGIN
    SELECT
        up.idUsuarioProducto,  -- AGREGADO: Campo idUsuarioProducto
        p.nombreProducto AS nombre_producto,
        p.descripcion,
        MIN(d.fechaVencimiento) AS fecha_vencimiento,
        up.stock
    FROM UsuarioProducto up
    INNER JOIN Producto p ON up.idProducto = p.idProducto
    LEFT JOIN Detalle d ON up.idUsuarioProducto = d.idUsuarioProducto
    WHERE up.idUsuario = p_idUsuario
    AND up.stock > 0
    GROUP BY up.idUsuarioProducto, p.nombreProducto, p.descripcion, up.stock
    ORDER BY p.nombreProducto;
END ;;
