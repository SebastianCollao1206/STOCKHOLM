CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_notificarProductosPorVencerParaUsuario`(
	IN p_idUsuario INT,
	IN p_dias INT
)
BEGIN
	-- 1. Variables
	DECLARE done INT DEFAULT 0;
	DECLARE v_idUsuarioProducto INT;
	DECLARE v_nombreProducto VARCHAR(50);
	DECLARE v_descripcion VARCHAR(50);
	DECLARE v_fechaVencimiento DATE;
	DECLARE v_titulo VARCHAR(100);
	DECLARE v_tipo VARCHAR(50);
	DECLARE v_descripcionFinal VARCHAR(500);

	-- 2. Cursor
	DECLARE cur CURSOR FOR
		SELECT 
			UP.idUsuarioProducto,
			P.nombreProducto,
			P.descripcion,
			MIN(D.fechaVencimiento)
		FROM UsuarioProducto UP
		JOIN Producto P ON P.idProducto = UP.idProducto
		JOIN Detalle D ON D.idUsuarioProducto = UP.idUsuarioProducto
		WHERE UP.idUsuario = p_idUsuario
		  AND UP.stock > 0
          -- AND d.restante > 0
		  AND D.fechaVencimiento > CURDATE()
		  AND D.fechaVencimiento <= DATE_ADD(CURDATE(), INTERVAL p_dias DAY)
		GROUP BY UP.idUsuarioProducto, P.nombreProducto, P.descripcion;

	-- 3. Handler (después del cursor)
	DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

	-- 4. Lógica
	OPEN cur;

	read_loop: LOOP
		FETCH cur INTO v_idUsuarioProducto, v_nombreProducto, v_descripcion, v_fechaVencimiento;
		IF done THEN
			LEAVE read_loop;
		END IF;

		SET v_titulo = CONCAT('Tu ', v_nombreProducto, ' ', v_descripcion, ' está por vencer');
		SET v_tipo = 'Urgente';
		SET v_descripcionFinal = CONCAT('Tu ', v_nombreProducto, ' ', v_descripcion, ' vencerá el ', DATE_FORMAT(v_fechaVencimiento, '%Y-%m-%d'));

		INSERT INTO Notificacion (
			idUsuario, titulo, tipo, descripcion, fechaNoti, estado
		) VALUES (
			p_idUsuario, v_titulo, v_tipo, v_descripcionFinal, NOW(), 'N'
		);
	END LOOP;

	CLOSE cur;
END ;;
