CREATE DEFINER=`root`@`localhost` PROCEDURE `usp_nuevoUsuarioEstablecimientoCompleto`(
    IN p_establecimiento   VARCHAR(100),
    IN p_direccion         VARCHAR(150),
    IN p_horaApertura      TIME,
    IN p_horaCierre        TIME,
    IN p_vendedor          VARCHAR(50),
    IN p_valoracion        DECIMAL(5,2),
    IN p_idUsuario         INT
)
BEGIN
    DECLARE v_idEstablecimiento INT DEFAULT NULL;
    DECLARE v_idVendedor        INT DEFAULT NULL;
    DECLARE v_idUsuarioEstab    INT;

    -- Si se proporciona nombre de establecimiento
    IF p_establecimiento IS NOT NULL AND p_establecimiento != '' THEN
        -- Buscar si ya existe el establecimiento con ese nombre y ese usuario
        SELECT E.idEstablecimiento INTO v_idEstablecimiento
        FROM Establecimiento E
        JOIN UsuarioEstablecimiento UE ON UE.idEstablecimiento = E.idEstablecimiento
        WHERE E.establecimiento = p_establecimiento AND UE.idUsuario = p_idUsuario
        LIMIT 1;

        -- Si no existe, lo insertamos
        IF v_idEstablecimiento IS NULL THEN
            INSERT INTO Establecimiento (establecimiento, direccion, horaApertura, horaCierre)
            VALUES (p_establecimiento, p_direccion, p_horaApertura, p_horaCierre);
            SET v_idEstablecimiento = LAST_INSERT_ID();
        END IF;
    END IF;

    -- Si se proporciona nombre de vendedor
    IF p_vendedor IS NOT NULL AND p_vendedor != '' THEN
        -- Buscar si ya existe ese vendedor para ese establecimiento
        IF v_idEstablecimiento IS NOT NULL THEN
            SELECT V.idVendedor INTO v_idVendedor
            FROM Vendedor V
            JOIN UsuarioEstablecimiento UE ON UE.idVendedor = V.idVendedor
            WHERE V.vendedor = p_vendedor AND UE.idEstablecimiento = v_idEstablecimiento
            LIMIT 1;
        ELSE
            SELECT idVendedor INTO v_idVendedor FROM Vendedor
            WHERE vendedor = p_vendedor
            LIMIT 1;
        END IF;

        -- Si no existe, insertarlo
        IF v_idVendedor IS NULL THEN
            INSERT INTO Vendedor (vendedor, valoracion)
            VALUES (p_vendedor, p_valoracion);
            SET v_idVendedor = LAST_INSERT_ID();
        END IF;
    END IF;

    -- Insertar la relación final si no existe
    SELECT idUsuarioEstablecimiento INTO v_idUsuarioEstab
    FROM UsuarioEstablecimiento
    WHERE idUsuario = p_idUsuario
      AND (idEstablecimiento <=> v_idEstablecimiento)
      AND (idVendedor <=> v_idVendedor)
    LIMIT 1;

    IF v_idUsuarioEstab IS NULL THEN
        INSERT INTO UsuarioEstablecimiento (idVendedor, idEstablecimiento, idUsuario)
        VALUES (v_idVendedor, v_idEstablecimiento, p_idUsuario);
    END IF;
END ;;
